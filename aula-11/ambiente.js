let contador = 1
// while = se ler assim : enquanto...
while (contador <= 6 ) {
  console.log (`Passo ${contador}`)  // o codigo se ler assim ;  enquanto a variavel contador for menor que 6, ele vai ficar executando o console.log(`Passo ${contador}`) até ele ficar maior ou igual a 6.
  contador++  // isso sgnfica contador = contador + 1     
                    //  aperte ctrl ç  e veja oque acontece
}



